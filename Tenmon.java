public class Tenmon {
    
    private String Ten;
    private String Loai;
    
    
    public Tenmon(String Ten, String Loai) {
        this.Ten = Ten;
        this.Loai = Loai;
    }
    public String getTen() {
        return Ten;
    }
    public void setTen(String Ten) {
        this.Ten = Ten;
    }
    public String getLoai() {
        return Loai;
    }
    public void setLoai(String Loai) {
        this.Loai = Loai;
    }

}



